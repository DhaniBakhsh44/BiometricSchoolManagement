    private void termListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_termListValueChanged
      TermBean termBean=(TermBean)termList.getSelectedValue();
      if(termBean==null)return;
       String month_Name[]={"Jan","Feb", "Mar"," Apr" ,"May","Jun" ,"Jul" ,"Aug" ,"Sep", "Oct", "Nov","Dec"};
      for(int i=0; i<month_Name.length; i++){
          if(termBean.getTermMonth()==i)
             monthTextField.setText(month_Name[i-1]);
      }
    
    }//GEN-LAST:event_termListValueChanged

