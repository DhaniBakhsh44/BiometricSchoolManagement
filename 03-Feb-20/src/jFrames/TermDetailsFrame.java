    private void addBtn4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtn4ActionPerformed
        int classId=((ClassesBean)classCombo.getSelectedItem()).getClassId();
        int schemeId=((SchemeBean)schemeCombo.getSelectedItem()).getSchemeId();
        String month=(String)termMonthCombo.getSelectedItem();
        int term = Integer.parseInt((String) termCombo.getSelectedItem());
       String month_Name[]={"Jan","Feb", "Mar"," Apr" ,"May","Jun" ,"Jul" ,"Aug" ,"Sep", "Oct", "Nov","Dec"};
        for(int i=0; i<termMonthCombo.getItemCount(); i++){     
        if(month.equals(month_Name[i])){
        int termMonth=i+1;
        addTerm(classId,schemeId,term,termMonth);
               }
        }
    }//GEN-LAST:event_addBtn4ActionPerformed

  
