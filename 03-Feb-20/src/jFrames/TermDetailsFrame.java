        private void loadComboBox(Vector<Object> obj,JComboBox combo){
        if(obj!=null)
          for(Object o:obj)
            combo.addItem(o);
    }
   
        
        private void getTermList(int classId,int schemeId){
            try{ 
         Vector term=DatabaseManager.getTermByClassId(classId,schemeId);
            termList.setListData(term);
            }catch(Exception e){
                e.printStackTrace();
            }
        }
        
 private void addTerm(int classId,int schemeId,int term,int termMonth){
            TermBean termBean=new TermBean();
            termBean.setClassId(classId);
            termBean.setSchemeId(schemeId);
            termBean.setTerm(term);
            termBean.setTermMonth(termMonth);
            termBean.setStatus(1);
            termBean.setRemarks(remarksField.getText());
             try {
                   int rows=DatabaseManager.addTerm(termBean);
                    if(rows>0)
                     JOptionPane.showMessageDialog(this,"Recod Insert Sucessfully...");
                     getTermList(classId,schemeId);
            } catch (SQLException ex) {
                 Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this,"This Term is Alread Insert in This Class");
                Logger.getLogger(TermDetailsFrame.class.getName()).log(Level.SEVERE, null, ex);
         }
  }
        
 private void deleteTerm(){
  SchemeBean schemeBean=(SchemeBean)schemeCombo.getSelectedItem();
  TermBean termBean=(TermBean)termList.getSelectedValue();
  if(termBean==null){
    Toolkit.getDefaultToolkit().beep(); 
    JOptionPane.showMessageDialog(this,"Kindly Select Student From List..");
    return;
  }
  
 try{
     int rows=DatabaseManager.deleteTermBySchemeId(schemeBean.getSchemeId(),termBean.getTerm());
     if(rows>0){
           Toolkit.getDefaultToolkit().beep();
         JOptionPane.showMessageDialog(this,"Delete Sucessfully...");
         getTermList(schemeBean.getClassId(), schemeBean.getSchemeId());
     }
 }catch(Exception e){
     e.printStackTrace();
 }
}
        
            
        

