    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
    ClassesBean classBean=(ClassesBean)classCombo.getSelectedItem();
    StudentRegistrationBean stdBean=(StudentRegistrationBean)stdCombo.getSelectedItem();
    SchemeBean schemeBean=(SchemeBean)schemeCombo.getSelectedItem();
    if(classBean==null)return; 
    if(stdBean==null)return; 
    if(schemeBean==null)return;
   StudentClassBean stdClassBean=new StudentClassBean();
     stdClassBean.setClassId(classBean.getClassId());
     stdClassBean.setRollNo(stdBean.getRollNo());
     stdClassBean.setStatus(stdBean.getStatus());
     stdClassBean.setSchemeId(schemeBean.getSchemeId());
      addStudentInClass(stdClassBean);
    }//GEN-LAST:event_addBtnActionPerformed

