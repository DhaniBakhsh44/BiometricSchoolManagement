     private void getStudentClasses(int class_id,int scheme_id){
        
         try{
             Vector v=DatabaseManager.getStudentClassByClassId(class_id,scheme_id);
             Vector temp=new Vector();
             for(int i=0; i<v.size(); i++){
                StudentClassBean stdBean=(StudentClassBean)v.elementAt(i);
                beans.StudentRegistrationBean stdRegisBean=DatabaseManager.getStudentRegistrationByRollNo(stdBean.getRollNo());
                temp.addElement(stdRegisBean);
             }
             stdClassList.setListData(temp);
         }catch(Exception e){
             e.printStackTrace();
         }
        
     }
     private void addStudentInClass(StudentClassBean stdClassBean){
    try{
         
        boolean check=DatabaseManager.checkStudentClass(stdClassBean);
        if(check){
            Toolkit.getDefaultToolkit().beep(); 
           JOptionPane.showMessageDialog(this,"This Student Is Already Insert in class");
        }
        else{
        int rows=DatabaseManager.addStudentClass(stdClassBean);
        if(rows>0)
            JOptionPane.showMessageDialog(this,"Data Insert Sucessfully..");
        getStudentClasses(stdClassBean.getClassId(),stdClassBean.getSchemeId());
        }
    }catch(Exception e){
        e.printStackTrace();
    }
    
}
     
 private void deleteStudentClass(){
      ClassesBean classBean=(ClassesBean)classCombo.getSelectedItem();
      SchemeBean schemeBean=(SchemeBean)schemeCombo.getSelectedItem();
      StudentRegistrationBean std=(StudentRegistrationBean)stdClassList.getSelectedValue();
    if(std==null){    
      Toolkit.getDefaultToolkit().beep(); 
      JOptionPane.showMessageDialog(this,"Kindly Select Student From List..");
       return;
         }
          try{
              int rows=DatabaseManager.deleteStudentClassByClassId(schemeBean.getSchemeId(),std.getRollNo());
              if(rows>0)JOptionPane.showMessageDialog(this,"Delete Sucessfully..");
              getStudentClasses(classBean.getClassId(),schemeBean.getSchemeId());
          }catch(Exception e){
              e.printStackTrace();
          }   
    }
     
